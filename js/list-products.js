function createIdProduct(){
    let idNewProduct = Math.floor(Math.random()*9999999999999 + new Date().getMilliseconds())
    for(let i=0; i<listAll.length; i++){
        if(idNewProduct == listAll[i].id ){
            createIdProduct()
        }
    }
    return idNewProduct;
}
// gọi hàm

